#!/bin/bash
# ============================================================
# start.sh вҖ” Ek command se pura project localhost pe chalao
# Usage: ./scripts/start.sh
# ============================================================

set -e

# Colors
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
RED='\033[0;31m'
CYAN='\033[0;36m'
NC='\033[0m'

PROJECT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"

echo -e "${CYAN}"
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•—     в–Ҳв–Ҳв•—     в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"
echo "  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ"
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  "
echo "  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•қ  в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•‘     в–Ҳв–Ҳв•”в•җв•җв•қ  "
echo "  в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"
echo "  в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ"
echo -e "${NC}"
echo -e "${BLUE}           рҹҸЎ Rivelle Stays вҖ” Local Dev Server${NC}"
echo "============================================================"

# в”Җв”Җв”Җ Start Frontend в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
start_frontend() {
  echo ""
  echo -e "${BLUE}вҡӣпёҸ  Frontend (React) start ho raha hai...${NC}"
  cd "$PROJECT_ROOT/frontend"

  if [ ! -d "node_modules" ]; then
    echo -e "${YELLOW}рҹ“Ұ Dependencies install ho rahi hain...${NC}"
    npm install --legacy-peer-deps
  fi

  echo -e "${GREEN}вң… Frontend port 3000 pe shuru ho raha hai...${NC}"
  npm start &
  FRONTEND_PID=$!
  echo "$FRONTEND_PID" > "$PROJECT_ROOT/.frontend.pid"
  echo -e "${GREEN}   Frontend PID: $FRONTEND_PID${NC}"
}

# в”Җв”Җв”Җ Cleanup on Ctrl+C в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
cleanup() {
  echo ""
  echo -e "${YELLOW}рҹӣ‘ Server band ho raha hai...${NC}"
  if [ -f "$PROJECT_ROOT/.frontend.pid" ]; then
    kill $(cat "$PROJECT_ROOT/.frontend.pid") 2>/dev/null || true
    rm "$PROJECT_ROOT/.frontend.pid"
  fi
  echo -e "${GREEN}вң… Sab kuch band ho gaya.${NC}"
  exit 0
}

trap cleanup SIGINT SIGTERM

# в”Җв”Җв”Җ Main в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
start_frontend

echo ""
echo -e "${GREEN}============================================================${NC}"
echo -e "${GREEN}рҹҺү Rivelle Stays chal raha hai!${NC}"
echo -e "${GREEN}   рҹҢҗ Frontend:  http://localhost:3000${NC}"
echo -e "${GREEN}============================================================${NC}"
echo -e "${YELLOW}   Ctrl+C dabaao band karne ke liye${NC}"
echo ""

# Keep script running
wait
